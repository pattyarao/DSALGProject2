public class Stack {

	private int n;
	private int count;
	private Node topNode;
	
	public int getN() {
		return n;
	}
	
	public void setN(int n) {
		this.n = n;
	}
	
	public int getCount() {
		return count;
	}
	
	public void setCount(int count) {
		this.count = count;
	}
	
	public Node getTopNode() {
		return topNode;
	}
	
	public void setTopNode(Node topNode) {
		this.topNode = topNode;
	}
}
